let idade = parseInt(prompt(`Digite sua idade:`))

if(idade <= 12){
    alert (`Parabéns, voê é uma crianca.`)
}

else if(idade <= 18 ){
    alert (`Você é um adolescente, meus pêsames.`)
}

else if(idade <= 59 ){
    alert (`Você é um adulto, meus pêsames.`)
}

else {
    alert (`Você é um idoso, meus pêsames.`)
}