let diaSemana = parseInt(prompt(`Digite um número de 1 a 7:`));

if(diaSemana === 1){
    alert (`Hoje é: Domingo.`);
}

else if(diaSemana === 2){
    alert (`Hoje é: Segunda-feira.`);
}

else if(diaSemana === 3){
    alert (`Hoje é: Terça-feira.`);
}

else if(diaSemana === 4){
    alert (`Hoje é: Quarta-feira.`);
}

else if(diaSemana === 5){
    alert (`Hoje é: Quinta-feira.`);
}

else if(diaSemana === 6){
    alert (`Hoje é: Sexta-feira.`);
}

else if(diaSemana === 7){
    alert (`Hoje é: Sábado.`);
}